package Lesson0;

public class Q0_3 {

	public static void main(String[] args) {
		// 計算させる
		
		System.out.print("39 + 2 = ");
		System.out.print(39 + 2);//足し算
		
		System.out.println();//改行
		
		System.out.print("39 - 2 = ");
		System.out.print(39 - 2);//引き算
		
		System.out.println();//改行
		
		System.out.print("-5 * 7 = ");
		System.out.print(-5 * 7);//掛け算
		
		System.out.println();//改行
		
		System.out.print("6 * 6 * 3.14 = ");
		System.out.print(6 * 6 * 3.14);//掛け算②
		
		System.out.println();//改行
		
		System.out.print("答えは" + 64);//文字列型と整数型の連結
		

	}

}
